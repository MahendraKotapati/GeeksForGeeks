bool find_path(Node *root,vector <int>&p,int key)
{
    if(root==NULL)
        return false;

    p.push_back(root->data);
    if(root->data==key)
        return true;

    if(find_path(root->left,p,key)||find_path(root->right,p,key))
        return true;
    
    p.pop_back();
    return false;
    
}
 
 
Node* lca(Node* root ,int n1 ,int n2)
{       
    vector <int>p1,p2; int i;
    find_path(root,p1,n1);
    find_path(root,p2,n2);

    for(i=0;i<p1.size() && i<p2.size();i++)
        if(p1[i]!=p2[i])
            break;
    
    return newNode(p1[i-1]);

}


